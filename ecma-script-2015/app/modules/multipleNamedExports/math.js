function add(n1, n2){
  return n1 + n2;
}

function subtract(n1, n2){
  return n1 - n2;
}

function divide(n1, n2){
  return n1 / n2;
}

function multpily(n1, n2){
  return n1 * n2;
}

function mod(n1, n2){
  return n1 % n2;
}

var pi = 3.14;

